package en.decorar;

public class Verbo {
	String verboPresente = "";
	String verboPassado = "";
	String verboParticipativo= "";
	
	
	public String getVerboPresente() {
		return verboPresente;
	}
	public void setVerboPresente(String verboPresente) {
		this.verboPresente = verboPresente;
	}
	public String getVerboPassado() {
		return verboPassado;
	}
	public void setVerboPassado(String verboPassado) {
		this.verboPassado = verboPassado;
	}
	public String getVerboParticipativo() {
		return verboParticipativo;
	}
	public void setVerboParticipativo(String verboParticipativo) {
		this.verboParticipativo = verboParticipativo;
	}
	
	
}
